function printPrice(price){
    let temp = [];
    let index=price.length+2;
    let count =1;
    for(let i = price.length-1;i>-1;i--){
        if(count%4==0){
            temp[index]='.';
            i++;
        }
        else{
            temp[index]=price[i]
        }
        count++;
        index--;
    }
    return temp.join('');
}
